text = "This is python course"

# Positive Indexing
print(text[0])  #The Output will be: 'T'
print(text[3])  # The Output will be: 's'

# Negative Indexing
print(text[-1])  # The Output will be: 'e'
print(text[-3])  # The Output will be: 'r'
 

